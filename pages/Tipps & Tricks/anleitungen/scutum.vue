      <h3 class="h4 mt-4">3. Ein erster Handgriff</h3>
      <ul>
        <li>
          Den exakten Mittelpunkt des Schilds auf der Vorderseite auf der Längs-
          und Querachse aufzeichnen und von dort aus einen Kreis aufzeichnen,
          der marginal größer ist als das Loch des Schildbuckels. Jetzt muss
          entlang der Horizontalen des späteren Schilds jeweils eine Linie
          oberhalb und unterhalb der Mitte gezogen werden (1,5-2cm Abstand zur
          Mitte). Dieser Steg wird später noch zusätzlich verstärkt.
        </li>
        <li>
          Entlang der Linien ausschneiden (wahlweise auf der designierten
          unteren Hälfte nicht ganz der Linie folgen, sondern den Kreis unten zu
          einer Geraden abändern
        </li>
        <li>
          Die Oberseite sehr gründlich feilen, sodass der Schild angenehm am
          langen Arm zu halten ist. Das heißt, dass der Schild an dieser Stelle
          also dem Verlauf der Haut folgen sollte und nicht eine Rundung oder
          gar Kante haben sollte.
        </li>
      </ul>

      <h3 class="h4 mt-4">4. Zusätzliche Stabilität</h3>
      <ul>
        <li>
          Die Innenseite mit Leinen auskleiden. Etwas von dem Wasserfesten Ponal
          mit Wasser verdünnen und gut vermischen (das kann man machen, solange
          der Ponal nicht schon mal getrocknet ist). Dann das Leinen mit dem
          Leim fest kleben. Ich habe dazu die Oberfläche von der Mitte aus mit
          dem Leim eingerieben. Wenn der Leim angetrocknet ist vorsichtig das
          Leinen über dem Loch für den Griff strahlenförmig einschneiden und
          durch das Loch umklappen.
        </li>
        <li>
          Als nächstes kann man die Verstärkung für den Griff anbringen. Ähnlich
          wie für das Holz für den Schild selbst sollte man das Holz entweder
          kochen oder lange einweichen. Dann die Grundlage und das Holz anleimen
          und auf die Biegekonstruktion spannen und aushärten lassen.
          Wahrscheinlich wird es ein bisschen schwierig, das Holz auf die
          Konstruktion zu bewegen, da jede Schicht Leinen das Holz beim
          Aushärten ein Bisschen in eine Richtung verbiegt, da es sich zusammen
          zieht. Wer ganz auf Nummer sicher gehen will, kann so eine Verstärkung
          auch an der Ober und Unterkante des Schildes zusätzlich anbringen
          (dann aber mindestens 3, besser 5 cm Abstand zum Rand lassen). Für
          diese und alle weiteren Verstärkungen in den folgenden Schritten
          empfiehlt sich, die nach oben liegenden Ecken und Kanten vor der
          Montage abzurunden.
        </li>
        <li>
          Als nächstes diese Verstrebung und weitere in der senkrechten des
          Schilds (entweder eine mittig und durch den Griff unterbrochen oder
          zwei entlang der Außenkanten mit Abstand zum Rand) mit den kleinen
          Nägeln sichern. Diese Nägel verstärken den Leim sehr gut. Sie werden
          von innen genagelt und können auf der Außenseite einfach umgeschlagen
          werden.
        </li>
        <li>
          Als nächstes die Außenseite mit Leinen einkleiden und verleimen. Das
          verdeckt die umgeschlagenen Nägel. Wenn man die richtige Art Farbe
          hat, kann man den Leim statt mit Wasser mit der Farbe verdünnen. Das
          Leinen bekommt so schon die Grundfarbe und die Farbe wird wasserfest
          (Wenn man wasserfesten Leim verwendet)
        </li>
      </ul>
