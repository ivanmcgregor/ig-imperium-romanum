      <h3 class="h4 mt-4">6. Die Umrandung</h3>
      <ul>
        <li>
          Den Hundekauknochen in warmem bis heißen Wasser über mehrere Stunden
          (z.B. über Nacht) einweichen lassen. Dann in die einzelnen Stücke
          zerlegen
        </li>
        <li>
          Die Rohaut in etwa 4-6cm breite Streifen schneiden (breiter, wenn man
          an den Seiten durch das Biegen einen größeren Abstand zwischen den
          Enden der Holzplatten hat). Dann anfangen, die Rohaut um den Schild zu
          legen und mit den Nägeln fest zu nageln. Dabei erst den Nagel durch
          Haut und Holz schlagen, dann auf der Rückseite die Rohaut spannen, mit
          einer Zange neben dem Nagel drücken, sodass der Nagel die Haut
          durchsticht und dann den Nagel sauber umbiegen. Hat mit einer
          Rohrzange sehr gut funktioniert. Die unterschiedlichen Streifen habe
          ich mit Überlappung befestigt, sodass Wasser nach unten abfließen
          würde. Abschließend habe ich noch Nägel mittig in die verbleibenden
          Abstände von der anderen Seite genagelt. Dann habe ich die Rohaut
          wieder trocknen lassen, was ihr schon sehr viel Stabilität gibt.
        </li>
        <li>
          Als nächsten Schritt habe ich einmal um die Rohaut außen herum genäht,
          um sie an einigen Stellen (wie den Rundungen und Übergängen)
          zusätzlich anzupressen und insgesamt zu sichern. Dazu habe ich die
          Sattlernaht verwendet. Ich musste leider 3mm Löcher vorbohren, da
          sonst die Nadel mit dem Faden nicht durch das Loch ging. Mit 2mm
          Löchern sähe es vielleicht ein bisschen besser aus.
        </li>
        <li>
          Als zusätzlichen Schritt kann man die Naht nochmal mit Leim
          überziehen, wenn man auch wirklich auf Nummer Sicher gehen will.
        </li>
      </ul>

