      <h3 class="h4 mt-4">5. Der Schildgriff</h3>
      <ul>
        <li>
          Auf der Außenseite ein weiteres Stück Holz anbringen und das ganze
          rund feilen, sodass es gut in der Hand liegt. Nach wunsch kann der
          Griff durch eine Lederumwicklung noch bequemer gemacht werden.
        </li>
        <li>
          Bei längerem Tragen wird der vorher gefeilte Übergang dennoch am
          Handgelenk einschneiden, sodass ein weiteres Polster notwendig werden
          wird. Entweder mehrere Lagen Leder oder eine Art Kissen direkt am
          Schild befestigt, oder aber eine dickere Ledermanschette, die man um
          den Arm trägt (diese sollte bis über den Handrücken reichen). Die
          Befestigung funktioniert entweder mit Kleber, kleinen Nägeln, oder
          indem man die Vernietung des Schildbuckels mit benutzt.
        </li>
      </ul>
