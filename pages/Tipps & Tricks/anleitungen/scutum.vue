      <h3 class="h4 mt-4">8. Schildbuckel anbringen</h3>
      <ul>
        <li>
          Die meisten Buckel haben vier Löcher in den Ecken vorgebohrt. Man kann
          noch zusätzliche Löcher in der Mitte bohren. Damit lässt sich die
          Querverstrebung auf der Rückseite mit fest nieten, was zusätzliche
          Stabilität gibt.
        </li>
        <li>
          Vermutlich muss als nächstes die Biegung der Basis an die des Schildes
          angepasst werden
        </li>
        <li>
          Damit das Holz sich nieten lässt, braucht man unbedingt
          Unterlegscheiben und muss den Kopf der Niete sehr vorsichtig pilzen,
          bevor der Schaft sich verbiegt und nicht hält. Alternativ kann man die
          Niete auch umhämmern, was auch hält – nur nicht so schön aussieht (Das
          ist auf der Innenseite aber eher egal)
        </li>
      </ul>

